#include <stdio.h>

int main() {
    float length, width, area;  //声明变量

    length = 5.0;
    width = 3.0;    //赋值
    area = length *width;   //计算面积

    printf("长方形的面积是：%.2f\n", area); //打印结果

    return 0;
}

