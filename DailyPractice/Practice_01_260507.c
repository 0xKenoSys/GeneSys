#include <stdio.h>
//一个图形程序要用结构来表示一个长方形
struct rect {
    long llx;   /* X coordinate of lower-left corner */ //long有符号，位置可以在负数象限
    long lly;   /* Y coordinate of lower-left corner */ //变量名缩写可以用多个单词开头字母小写
    unsigned long width;    /* Width(in pixels) */   //how to show the rect in pixels?使用操作系统提供的图形库，读取结构体里的数据，由显卡渲染图形
    unsigned long height;   /* Height (in pixels) */
    unsigned color; /* Coding of color */
};

int main() {
    //可以声明一个struct rect类型的变量r，并将它的字段设置如下：
    struct rect r;  //这一句就是真正向系统申请了一块内存（通常分配在栈区），用来存放一个具体的矩形，并给这块内存起名叫 r
    r.llx = r.lly = 0;
    r.color = 0xFF00FF;
    r.width = 10;
    r.height = 20;

    //这里表达式r.llx就会选择结构r的llx字段
    //另外，我们可以在一条语句中既声明变量又初始化它的字段：
    struct rect r ={ 0, 0, 10, 20, 0xFF00FF };

    //将指向结构的指针从一个地方传递到另一个地方，而不是复制它们，这是很常见的。例如，下面的函数计算长方形的面积，这里，传递给函数的就是一个指向长方形struct的指针

    //表达式(*rp).width间接引用了这个指针，并且选取所得结构的width字段，这里必须要用括号，因为编译器会将表达式*rp.width解释为* (rp.width)，而这是非法的。
    //间接引用和字段选取结合起来使用非常常见，以至于C语言提供了一种替代的表示法->。即rp->width等价于表达式(*rp).width。例如，我们可以写一个函数，它将一个长方形逆时针旋转90度。
    //rp->width 在底层机器码上和 (*rp).width 没有任何区别，绝对等价。 它的具体意思非常直白：顺着 rp 这根指针（箭头），直奔它所指向的结构体，并把里面的 width 字段抓取出来。

    void rotate_left(struct rect *rp) {
        /* Exchange width and height */
        long t = rp->height;
        rp->height = rp->width; //这是啥操作？->是啥啊？答：间接引用和字段选取结合。可是具体是啥意思呢？“取字段”的本质，是编译器在后台替你完成了一次极其精确的“坐标偏移计算”，然后直接暴力推开了那扇房门，把里面的数据拿出来，或者把新数据砸进去。
        rp->width = t;          //rp->width 的结果，绝不是地址，而是那块内存里的“数据本身”（也就是值），或者是那块内存“实体容器”（当它在等号左边时）。这里涉及到底层语法中非常关键的左值（L-value）和右值（R-value）概念：当取字段发生在等号右边时（取值）比如你写的 long t = rp->width;这里的“取字段”，是计算机走到 0x1010 这个地址，把里面存放的那个数字（比如你之前初始化的 10），从这 8 个字节的房间里端出来，复制一份，交给了临时变量 t。你在这里取的是纯粹的数据值，而不是房间的门牌号。
        //当取字段发生在等号左边时（取容器）比如你写的 rp->width = t;。这里的“取字段”，是计算机走到 0x1010 这个地址，一把拉开房门，把里面原本装的数字清空，然后把你手里的数字 t 强行塞进这 8 个字节的空间里去。你在这里取的是这块物理内存的控制权。
        //如果你真的想要获取 width 这个变量的“地址”，即你只想要 0x1010 这个门牌号，而不想管里面装了什么，你必须在整个表达式的前面加上取址符 &，明确写成：&(rp->width)
        /* Shift to new lower-left corner */
        rp->llx -= t;   //-=是什么？
    }

    return 0;
}
