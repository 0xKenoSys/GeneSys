//在我们刚刚的推算中，我告诉你偏移量是直接把前面变量的字节数相加。但在真实的硬件世界中，CPU 在读取内存时是非常挑剔的。为了最高效地抓取数据，CPU 往往要求某些数据类型必须放在特定倍数的地址上（比如 8 字节的 long 最好放在地址是 8 的倍数的地方）。这种机制叫做内存对齐（Memory Alignment）。
//这就导致了一个现象：排屋里的房间，并不总是墙贴着墙的。
//如果你的结构体里，第一个元素是 1 个字节的 char，第二个元素是 8 个字节的 long。编译器在安排房间时，不会让 long 紧贴着 char。它会在 char 后面偷偷塞进去 7 个毫无意义的废弃字节（称为 Padding），作为填充缓冲带，然后才在对齐的边界上建造 long 的房间。
//这意味着，如果你想去“取”那个 long 的字段，它的偏移量不是 1，而是 8。
//这也是为什么“取字段”这个动作绝对不能由人类手动去算偏移量（就像我上一层演示的极端代码那样），因为由于不同架构（32位或64位）、不同编译器的对齐规则不同，各个房间的偏移量在不同机器上很可能是完全不一样的。

//指针 rp 是一根极细的牵引线，一头捏在你的代码逻辑里，另一头死死拴在内存深处一个连体盲盒（结构体）的第一个格子上。
//“取字段”（->）这个动作，就是你顺着这根线摸到这个盲盒，然后以不容置疑的语气下达指令：“替我算好步数，直接打开名为 width 的那个格子。如果是读取，就把里面的东西原样端给我；如果是赋值，就把我手里的东西按进去。”
