//一个图形程序要用结构来表示一个长方形
struct rect {
    long llx;   /* X coordinate of lower-left corner */ //long有符号，位置可以在负数象限
    long lly;   /* Y coordinate of lower-left corner */ //变量名缩写可以用多个单词开头字母小写
    unsigned long width;    /* Width(in pixels) */   //how to show the rect in pixels?使用操作系统提供的图形库，读取结构体里的数据，由显卡渲染图形
    unsigned long height;   /* Height (in pixels) */
    unsigned color; /* Coding of color */
};
    //在 C 语言的世界里，如果你把一个指针递给一个函数，就相当于把保险箱的钥匙给了别人，存在被恶意篡改的风险。为了防御这种风险，对于只读不写的函数参数，我们应该在指针前面加上 const 关键字
long area(const struct rect *rp) {    //调用函数时，计算机会把整个®结构体（好几十个字节的数据）原封不动地复制一份传过去。这不仅浪费内存，还消耗 CPU 周期。而传递指针（也就是传递这个结构体在内存中的首地址），不管结构体多庞大，传过去的总是一个固定大小（通常是 8 字节）的内存地址，效率极高。
    return (*rp).width * (*rp).height;  //居然可以直接return一个计算式子？在编译器的视角里，return 后面跟着的永远是一个“表达式”。计算机会先在 CPU 的寄存器里（比如 ALU 算术逻辑单元）把 width * height 的结果算出来，得到一个最终的值，然后直接把这个值返回。
}

