//Two sum
//Given an array of integers nums and an integer target, return indices of the two numbers such that they add up to target.

//You may assume that each input would have exactly one solution, and you may not use the same element twice.

//You can return the answer in any order.

//Example 1:

//Input: nums = [2,7,11,15], target = 9
//Output: [0,1]
//Explanation: Because nums[0] + nums[1] == 9, we return [0, 1].

//two for-loops
//提问：咋映射过去呢？indices的中括号里的内容，和指针的偏移量如何建立映射？//回答：给中括号里的内容分配一块两个整型值的内存（解答了结果如何返回，但它没有解释寻找映射的过程）
//在双重循环中：
//输入映射：依靠循环变量的递增实现
//查找逻辑：依靠CPU的算术逻辑单元（ALU）进行加法比较
//存储映射：分配两个int的空间
//注意：一定区分寻址与存储。一定追问底层映射关系。

//1.数组索引与指针偏移量的本质联系：
//在C或C++这种底层语言中，数组并不是一个复杂的数据结构，它本质上是一段连续的内存空间。
//当写出nums[i]时，编译器实际上执行的是一个简单的算术运算。假设nums是指向数组首地址的指针，那么：Address(nums[i])=BaseAddress(nums)+i*sizeof(int)，这里的i就是问题中“中括号里的内容”，而它与内存的映射关系就是偏移量(Offset)。
//为什么是“两个整型值的内存”？
//在Two Sum问题的双重循环解法中，我们的目标是找到两个索引i和j，使得nums[i]+nums[j]==target。一旦找到了这两个值，我们需要把它们返回。
//由于函数通常只能返回一个值，如果我们想返回两个索引，就必须返回一个指针或者一个容器（如std::vector）。在C语言的Leetcode环境中，通常要求使用malloc动态分配一块内存。
//所以，回答所说的“分配两个整型值的内存”是指结果数组的内存。而“中括号里的内容”（即i和j）到“内存”的映射，其实分两个维度：1.输入端映射：通过循环变量i偏移指针找到nums中的数值。2.输出端映射：将找到的i和j存入新分配的那两块int大小的内存空间中。

//2.深入探讨双重循环的执行路径
//在这个过程中，i和j并不是某种抽象的“键”，它们直接代表了物理距离。外层循环i选定第一个加数，内层循环j在i之后的区间寻找补数。
//当nums[i]+nums[j]==target成立时，当前的i和j就是我们寻找的坐标。此时，我们需要把这两个位于CPU寄存器或栈空间里的局部变量，转移到堆内存中

//3.纠正与深化：什么是真正的“映射”？
//在双重循环中，其实并没有利用到哈希表那种复杂的哈希映射。双重循环的本质是穷举偏移量，是在通过不断改变i和j的值，去探测内存地址上的数值。哈希表的本质才是值与索引的映射。
//如果按照“分配两个整型值的内存”就是映射的全部，那可能把问题想得太简单了。在更高效的O(n)解法中，我们会使用哈希表，那时候，“中括号里的内容”会变成nums[i]的数值，而“映射的结果”才是该数值对应的索引.
//两者的对比：
//(1)双重循环：
//动作：我知道位置（i），去查数值nums[i]
//内存：只需要结果输出时分配2个int的空间
//代价：时间极慢，因为要反复扫描
//(2)哈希表：
//动作：我知道需要的数值target-nums[i]，去查它在哪个位置
//内存：需要分配一个能容纳n个键值对的哈希映射表空间
//代价：空间换时间

//4.需要注意的工程细节
//第一：中括号的语法糖
//在C语言中，*(indices+0)和indices[0]是完全等价的。当思考如何“映射”时，脑子里应该浮现指针在内存条上滑动的画面。指针每增加1，实际上地址增加了sizeof(type)个字节
//第二：返回大小
//别忘了Leetcode的函数签名通常包含int* returnSize。这又是一个映射：不仅要返回内存地址，还要通过指针告诉调用者，你分配了多少个元素的空间
//*returnSize = 2; 这也是一种映射，将数值2写入调用者提供的地址

//Created by Madeleine on 2026/3/24.

//从内存和处理器之间看四阶段--探测（寻址）->计算（ALU）->固化（分配与存储）->移交（返回的指针）
//1.偏移量的本质：基址寻址
//在双重循环里，所谓“挨个增加偏移量”，在CPU里其实是一次又一次的有效地址计算
//当写下nums[i]时，编译器会把它翻译成类似[base + index * scale]的指令。这里的base时数组的首地址，index就是循环变量i或者j，而scale时数据类型的长度（对于int来说是4字节）
//硬件层面：内存控制器根据计算出的地址，把对应的数据从内存（或者更快的L1/L2缓存）加载到CPU的寄存器中
//物理映射：循环变量的每一次递增，本质上是让读写头在内存空间里向高地址方向移动了4个单位

//2.ALU的博弈：加法与标志位
//一旦数据进入寄存器，就轮到CPU的核心部件--算术逻辑单元ALU--上场
//在Two Sum逻辑中，ALU实际上执行了两步动作
//加法指令ADD：它把nums[i]取出的值和从nums[j]取出的值进行二进制加法运算
//比较指令CMP：ALU会把加法的结果减去target，如果相减的结果为0，CPU状态寄存器里的ZF（零标志位）就会被置为1.
//这就是“加法比较”，代码里的if(sum == target)在底层其实就是一次对零标志位的判断。如果标志位亮了，说明找到了那两个偏移量。

//3.空间的分配：堆内存的“永存”
//当确定了i和j之后，接下来的动作就是“装载”，但这里有个很关键的细节：为什么要专门分配空间？
//如果我们在函数内部定义一个普通的数组int result[2]，这个数组是分配在栈上的，一旦函数执行完毕，这块内存就会被回收，调用者拿到的指针就会指向一个无效的地址。
//所以，需要在用malloc在堆上切出一块8字节的空间，这块内存除非手动释放，否则它会一直呆在那儿
//赋值操作：把寄存器里的变量i的值，写入到新分配内存的第一个4字节空间；把j的值，写入第二个4字节空间
//映射建立：这时候，原本只是循环计数器的i和j，正式变成了被持久化记录的“结果数据”

//4.输出：传递地址
//在C语言的逻辑里，函数返回的并不是那两个数字本身，而是那块内存的首地址。调用者拿到这个地址，再通过相同的偏移量规则（result[0]和result[1]），就能从内存把那两个索引值取出来



#include<stdio.h>
#include <stdlib.h>

/*int* nums：传入的整型数组，数组作为参数传递时，会自动退化为指针，指向数组的第一个元素。因此我们在函数内部无法直接通过sizeof算出数组的整体长度。*/
/*int numsSize: 正是因为上面的指针退化现象，调用者必须显式地把数组的长度传进来，告诉我们这个数组里到底有多少个元素，防止我们越界访问*/
/*int target: 这是我们要寻找的目标和*/
/*int* returnSize: 这是一个关键的输出参数（Out Parameter），C语言的函数只能有一个返回值，我们已经用它来返回数组的首地址了，但调用方（也就是Leetcode的判题系统）还需要知道你返回的这个数组到底有多长。所以，调用方传递了一个变量的地址进来，我们需要在函数内部通过解引用，把实际的返回长度（找到答案就是2，没找到就是0）写进这个地址里去*/
//if (nums[j] == target - nums[i]): x = target - current 要去找的元素应该是目标减去现在指向的元素
    int* twoSum(int* nums,int numsSize, int target, int* returnSize) {
        for (int i = 0; i < numsSize; i++) {    //这是最外层的循环。变量i代表当前考察的第一个数字的索引。我们从数组的最开头，也就是索引0开始，一直遍历到数组的末尾。只要i还没有到达numsSize，循环就会继续。这个外层循环的作用是“固定”一个基准元素
            for (int j = i + 1; j < numsSize; j++) {    //这是内层循环。注意看它的初始化条件：int j = i + 1。这非常重要。为什么不从0开始？因为题目通常要求不能重复使用同一个元素，如果我们从0开始，当i和j相等时，我们就会错误地把同一个数字加了两次。为什么是i + 1？因为i之前的元素已经在之前的一轮外层循环中和所有其他元素匹配过了，如果我们从头开始匹配，就是在做无用功。这里的设计保证了我们只遍历所有可能的“组合”，而不是“排列”，极大地减少了不必要的计算
                if (nums[j] == target - nums[i]) {  //这是逻辑判断的核心。我们在检查当前的两个元素之和是否等于目标值。注意它的写法，它并没有写成nums[i]+nums[j]==target，而是将加法移项变成了减法。虽然在普通的刷题场景下，两者等效，但在严谨的工程实践中，两个较大的整数相加可能会触发整数溢出（integer overflow），导致程序崩溃或出现逻辑错误。而用目标值减去其中一个数再去比较，则规避了这种潜在的风险。
                    int* result = malloc(sizeof(int) * 2);  //这一是整段代码最能体现底层内存管理机制的地方。当我们找到了符合条件的两个索引时，我们需要把它们装进一个数组里返回，但是我们绝对不能写成int result[2]。为什么？因为如果只声明一个普通的局部数组，它会被分配在系统的“栈区”。当这个函数执行到最后的return并结束时，它所在的栈帧会被立刻销毁，局部数组的内存也会被系统回收。如果你把指向这块被回收内存的指针返回给了调用方，调用方拿到的就是一个“悬垂指针（Dangling Pointer）”，去访问它会导致未定义行为。
                                                                //为了解决这个问题，我们必须使用malloc函数向系统申请分配在“堆区”的内存。堆内存的生命周期是手动的，只要我们不主动调用free去释放它，它就会一直存在，这样调用方就能安全地读取里面的数据了。sizeof(int)会计算出当前系统架构下一个整型占据的字节数，乘以2就是我们需要容纳两个索引的总字节数。
                                                                //我觉得这个堆和栈的问题很关键，我对函数这个东西的使用理解还不深，稍后去查
                    result[0] = i;  //内存分配成功后，我们拿到了指向这块堆内存首地址的指针result。底层语言允许我们将指针作为数组名来使用，这叫作指针算术的语法糖。我们将外层循环的当前索引i存入第一个位置，将内层循环的当前索引j存入第二个位置。这就是我们最终要交付的答案。
                    result[1] = j;
                    *returnSize = 2;    //使用第一行的int* returnSize。因为调用方需要知道我们返回的数组有几个元素，所以我们对这个指针进行解引用（加上星号*），将数值2直接写入调用方提供的内存地址中。这样，即使函数结束，调用方也能准确知道它该读取几个数据。
                    return result;      //找到了答案，分配了内存，填好了数据，也告诉了调用方长度。接下来就是干净利落地把指向答案堆内存的指针返回，同时结束整个函数的执行流程。
                }
            }
        }   //这三个反括号分别闭合了if判断、内层循环和外层循环的代码块。它们界定了变量的作用域，并控制着程序的执行流向。

        //Return an empty array if no solution is found
        *returnSize = 0;    //代码执行到这里，意味着上面的双重循环全部跑完，却依然没有触发那个return result;。说明在这个数组中，根本不存在两个数字的和等于target。这是一个边缘情况(edge case)。按照约定，如果没有找到解，我们需要告诉调用方返回的数组长度是0.所以我们再次解引用，将0赋值进去。
        return malloc(sizeof(int) * 0); //这是函数的最后一步。既然没有找到解，我们要返回一个空数组。这里它使用了malloc(sizeof(int)*0)，这种写法其实有些取巧，在标准规定中，分配零字节内存的行为是依赖于具体实现的，它可能会返回一个空指针NULL，也可能返回一个唯一的、合法的但不允许访问的指针。
                                            //Leetcode的判题后端在某些测试用例中，需要拿到一个合法的非空指针来进行内部处理，所以这里选择分配并返回一个大小为0的堆内存指针。在实际的工程开发中，如果没有找到结果，通常直接返回NULL会是更明确、更不易出错的做法。
    }   //这段代码的时间复杂度是O(N^2)，因为有两层嵌套循环，当数组极度庞大时，它的运行效率会呈现指数级下降。空间复杂度是O(1)，因为我们除了分配最后返回结果所需的常数级别内存以外，没有使用任何额外的数据结构。
        //早点把哈希表O(N)的优化版写出来
int main() {
        int nums[] = {2, 7, 11, 15};
        int target = 9;
        int returnSize;

        int* result = twoSum(nums, 4, target, &returnSize); //全是变量名

        if (returnSize == 2) {
            printf("Indices: %d, %d\n", result[0], result[1]);
        }else {
            printf("No solution found.\n");
        }

        free(result);
        return 0;
    }














