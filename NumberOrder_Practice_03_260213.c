#include <stdio.h>
int main() {
    int a, b, c, t;

    printf("Enter three integers:\n");
    scanf("%d %d %d", &a, &b, &c);

    if (a > b) {    /*如果a大于b,借助中间变量t实现a与b值的互换*/
        t = a;
        a = b;
        b = t;  //我没看明白这一步。t拿起了要放下，卸载
    }

    if (a > c) {    /*如果a大于c,借助中间变景t实现a与c值的互换*/
        t = a;
        a = c;
        c = t;  //思路：优先保证a的位置是最小的，把最小的数固定下来，然后更大的数被t运走拿到左边
    }
    if (b > c) {    /*如果b大于c,借助中间变量t实现b与c值的互换*/
        t = b;
        b = c;
        c = t;
    }
    printf("The order of the number is:\n%d %d %d", a, b, c);
    return 0;
}

//任意输入 3 个整数，编程实现对这 3 个整数由小到大进行排序。
