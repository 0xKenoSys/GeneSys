//(1)定义数据类型，本实例中 a、b、c、t 均为基本整型。

//(2) 使用输入函数获得任意 3 个值赋给 a、b、c。

//(3) 使用 if 语句进行条件判断，如果 a 大于 b，则借助于中间变量 t 互换 a 与 b 值， 依此类推比较 a 与 c、b 与 c，最终结果即为 a、b、c 的升序排列。

